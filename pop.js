//pop()- Mutating

//pop() method is used to remove the last element from an array and return that element

//It takes no parameters.
//It returns the removed element from the array.
//If the array is empty, pop() returns undefined.


//Ex 1
const arr = []

const rs = arr.pop()

console.log(rs); //undefined

//Ex 2
const bike = ['Yamaha','Bajaj','Honda','TVS']

const result = bike.pop()

console.log(result); // TVS

console.log(bike); // ['Yamaha','Bajaj','Honda']



